                  <div class="post-text text-style">
                     <p><strong>Ayahuasca </strong>Amazon ormanlarında bulunan kutsal <strong>Ayahuasca bitkisi 'Dimethyltryptamine</strong>' molökülü yani kısaca<strong> 'DMT' </strong>olarak bilinen,en güçlü 'psychedelic' maddedir! ve her organizma kendi içerisinde bu sentezlerden taşır.Embrio halindeki bir bebeğin sekiz haftaya kadar salgıladığı bir sıvıdır üstelik.İnsan dediğimiz varlığın hayat yolculuğunda kendisini keşfetmesi gerekir.Hayattaki en önemli bir detaydır!Bilinç altı,bilinç üstü,bilinç ötesi olarak nitelendirirsek ruh,beden,akıl,paralel evrenler,şartlanmalar,çekim yasası,sevginin gücü,enerji teorisi,inanç ve <strong>Tanrı</strong> kavramları yüksek bilinçimiz sayesinde vardırlar. </p>
                     <h2>Yeniden doğuş. </h2>
                     <p><strong>DMT</strong> maddesi iki gözün arasındaki alın boşluğunda bulunan bir sıvıdır.Biz insanlar bu sıvıyı kendi bilincimizin yettiği kadar kullanabiliyoruz.Birşey varki geçmişte yapmış olduğumuz hatalar veya değişim isteği içerisine girdiğimiz dönemlerde tekrardan başa sarmamızla sonuçlanıyor.Deneyimlediğim ve cevremdede yeniden doğduk,ben başka bir insan oldum dedirten cinsden bir bitki <strong>ayahuasca.</strong>Üçüncü gözünüzü açtıran<strong> ayahuasca</strong> bitkisini aldığınız zaman adeta yeniden doğuyorsunuz.Hayat felsefeniz,dünyaya bakışınız değişiyor.Terim yerinde yeniden doğuyorsunuz! </p>
                     <h2>Tavsiye ediliyor.  </h2>
                     <p><strong>Spiritüe</strong>l tarafının yanı sıra fiziksel iyileştirici özelliği ile her geçen gün daha fazla kişiyi iyileştiriyor.Piskiyatristler ve bilim adamları tarafından incelenip deneyimlendikden sonra piskologlar tarafından tavsiye ediliyor.Bizzat kendi piskoloğum'un bir <strong>seromoni</strong>ye katılmamı tavsiye etmesiyle korkarakda olsa deneyimini yaşadım.Hayatın anlamını sorgulayan içimizi kemirip bitiren sorulardan,egolardan,çocukluk travmalarından,endişelerden,bağımlılıklardan kurtulmak isteyen hayat kurtarıcısı ve hayat değiştiren bir bitki <strong>Ayahuasca</strong>.İki sene boyunca her hafta gittiğim piskiyatri görüşmelerim artık son buldu,çünkü<strong> Ayahuasca</strong> 10 senelik pisikotrepi seansına bedel.Bu deneyimi kendi ülkemiz olan Türkiye'de gerçekleştirdim.Elbetteki ana vatanı olan <strong>Amazon</strong> ormanlarında yaşamak bir başka olacakdır.Gidip tecrübe edip yeniden yazmak dilekleriyle inşallah.. </p>
                     <h2>Eski Türkler Şaman olarak bilinen 'Kham'dı. </h2>
                     <p>İnsanoğlu binlerce yıl biriktirdiği ve yaşadıkları gerçekleri görmezden geliyor nedense!<strong>Şamanizim</strong>bir Din değildir!<strong>Şamanizim</strong> dinin uygulanış biçimidir.Ayrıca bütün dünyadaki dinlerin çıkış noktasıdır.<strong>Şamanizim</strong> Derin bir ilme sahip olmayı gerektirmeyen olduğun gibi hayatı yaşamak ve doğa ile bütünleşmek için yüz yıllardır var olan ruhani bir kavramdır.<strong>Orta asya ile kuzey asyada boy gösterip;Özbekistan,Kırgızistan,Tacikistan,Türkmenistan,Afganistan ve Çin'in bir kısmı olan ülkeler merkezidir..ve biz Türk'ler orta asyadan göç edip şamanizimden müslümanlığa geçiş yapmış insanlarız. </strong></p>
                     <h2>Evren üç katmandır. </h2>
                     <p>Dünyamız üç katmandan oluşur.Gökyüzü,yer altı ve yer üstü olmak üzere üç bölümdür.Üst katman olan gökyüzünde Şamanlar tarafından gök <strong>Tanrı</strong>sı olarak bilinen ve biz günümüz insanlarında bildiği ve sonsuz inandığı <strong>Allah'ın</strong> varlığı,yeryüzünde biz canlılar ve yer altında ise bu dünyadan geçip artık göç etmiş olan ölülerimiz vardır.<strong>Şamanlar</strong> doğa aleminde yaşımı idrak edip,doğaya hükmeden gücü sezmişdirler.Yağmur,kar,fırtına,güneş,rüzgar gibi doğa eylemlerini benimsemiş ve en duru insan olarak günümüzde halen hayatlarını sürdürmeyi başarmışlardır.<strong>Ayahuasca bitkisi</strong>çayı sayesinde gerçekleştirilen yolculuk gerçekden ruhani bir boyutdur.Yeryüzünde bulunan biz insanları yer altınada,gökyüzünede çıkarttığı kuşkusuz bir gerçekdir. </p>
                     <h2>Bunları biliyormuydunuz?</h2>
                     <p>İnsanoğlu tarafından <strong>şamanizim</strong> inkar edilir?Peki neden? Oysaki farkında olsakda,olmasakda kültürümüzün,yaşayışımızın gelenek ve göreneklerimizin temelinde <strong>Şamanizim</strong> vardır.Örnek verecek olursam,aşağıda sayacağım maddeler günümüze atalarımız olan <strong>Şaman</strong> yani '<strong>Khan</strong>'lardan miras kalmışdır.</p>
                     <p><strong>*Nişan yüzüklerine takılan kırmızı kurdele. </strong></p>
                     <p><strong>*Kurşun dökmek.</strong></p>
                     <p><strong>*Mezar taşlarımız. </strong></p>
                     <p><strong>*Dilek tutmak. </strong></p>
                     <p><strong>*Köpek ulumasının uğursuz sayılması. </strong></p>
                     <p><strong>*Nazar boncuğu(Nazar inancımız) </strong></p>
                     <p><strong>*Kullandığımız kilim motifleri. </strong></p>
                     <p><strong>*Mevlit ve ilahiler. </strong></p>
                     <p><strong>*Su içerken kafanın el ile desteklenmesi. </strong></p>
                     <p><strong>*Kuşların içmesi için mezarlardaki küçük su bölmeleri.</strong></p>
                     <p><strong>*Yukarıda Allah var sözü. </strong></p>
                     <p><strong>*Sağ ayak ile adım atma geleneği. </strong></p>
                     <p><strong>*Su dökerek yolcuyu uğurlama. </strong></p>
                     <p><strong>*Türbelere ağaçlara bez parçası bağlamak. </strong></p>
                     <p><strong>*Üç kere tahtaya vurmak. </strong></p>
                     <p><strong>*Çocuklara doğadan esinlenerek isimler koymak. </strong></p>
